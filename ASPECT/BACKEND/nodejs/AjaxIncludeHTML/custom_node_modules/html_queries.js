exports.htmlqueries= {HTML_Query,JSLIB_Query,CSS_Query};

var fs = require('fs');
function HTML_Query(req, res, filepath) {
    fs.readFile(filepath, function (err, data) {
      res.writeHead(200, { 'Content-Type': 'text/html' });
      res.write(data);
      res.end();
    });
  }
